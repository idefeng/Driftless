export const translations: Record<AppLanguage, Record<I18nKey, string>> = { zh, en };

export function translate(language: AppLanguage, key: I18nKey, params: TranslationParams = {}): string {
  return translations[language][key].replace(/\{(\w+)\}/g, (match, name) => {
    const value = params[name];
    return value == null ? match : String(value);
  });
}
